package com.example.baked.models;

public class Step {
    int id;
    String shortDescription;
    String description;
    String videoURL;
    String thumbnailURL;


    public int getId() {
        return id;
    }

    public String getShortDescription() {
        return shortDescription;
    }

    public String getDescription() {
        return description;
    }

    public String getVideoURL() {
        return videoURL;
    }

    public String getThumbnailURL() {
        return thumbnailURL;
    }
}
